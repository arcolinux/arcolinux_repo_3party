echo "Let us sign the packages without a .sig"
echo "#############################################################################################"

count=0
cd x86_64
for name in $(ls *.tar.zst); do
	count=$[count+1]
	if [ ! -f $name.sig ];then	
		tput setaf 1;echo $count " : signing " $name;tput sgr0;
		gpg --detach-sign $name
	fi
done

echo "#############################################################################################"
echo "Signing finished"
echo "#############################################################################################"